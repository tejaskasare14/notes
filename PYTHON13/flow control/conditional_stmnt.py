# if, if-else, if-elif(n)-else

#check for even number
a = 10
if(a%2==0):
   print(a," is even")
   
#check for even or odd
b=130
if(b%2==0):
   print(b, "is even")
else:
   print(b, "is odd")
   
#check for positive, negative or zero
c=20
if(c>0):
   print(c, "is positive")
elif(c<0):
   print(c, "is negative")
else:
   print(c, "is zero")