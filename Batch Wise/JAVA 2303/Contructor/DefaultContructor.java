public class DefaultContructor {
   byte a ;
   short b;
   int c;
   long d;
   float e;
   double f;
   boolean g;
   char h;
   String i;
   public static void main(String[] args) {
      DefaultContructor dc = new DefaultContructor();
      System.out.println(dc.a);
      System.out.println(dc.b);
      System.out.println(dc.c);

      System.out.println(dc.d);

      System.out.println(dc.e);
      System.out.println(dc.f);

      System.out.println(dc.g);
      System.out.println(dc.h);
      System.out.println(dc.i);
   }
}
